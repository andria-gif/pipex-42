#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	const char			*s;
	char				*d;
	size_t				i;

	d = (char *) dest;
	s = (const char *) src;
	i = 0;
	if (!dest && !src)
		return (NULL);
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (d);
}
